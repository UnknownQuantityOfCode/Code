var twoSum = function(nums, target) {
    for (var i = 0; i < nums.length; i++) {
    	for (var j = 0; j < nums.length; j++) {
	    	if((nums[i]+nums[j]) == target){
	    		return [i,j]
	    	}
	    };
    };
    return [];
};

var twoSumMapped = function(nums, target) {
	var map = {};
	nums.forEach(function(i, j) { map[i] = j+1 });
    for (var i = 0; i < nums.length; i++) {
    	var complement = target - nums[i];
    	if(map[complement]){
    		return [i,(map[complement]-1)].sort();
    	}
    };
    return [];
};

